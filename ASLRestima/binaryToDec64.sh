#!/bin/bash
line1=""
line2=""
line3=""
line4=""
line5=""
line6=""
line7=""
line8=""
while read line
do
  read -ra ADDR1 <<< "$((2#${line:0:8}))"
  read -ra ADDR2 <<< "$((2#${line:8:8}))"
  read -ra ADDR3 <<< "$((2#${line:16:8}))"
  read -ra ADDR4 <<< "$((2#${line:24:8}))"
  read -ra ADDR5 <<< "$((2#${line:32:8}))"
  read -ra ADDR6 <<< "$((2#${line:40:8}))"
  read -ra ADDR7 <<< "$((2#${line:48:8}))"
  read -ra ADDR8 <<< "$((2#${line:56:8}))"
  line1="$line1, ${ADDR1}"
  line2="$line2, ${ADDR2}"
  line3="$line3, ${ADDR3}"
  line4="$line4, ${ADDR4}"
  line5="$line5, ${ADDR5}"
  line6="$line6, ${ADDR6}"
  line7="$line7, ${ADDR7}"
  line8="$line8, ${ADDR8}"
done < $1
echo "${1:8}"
echo "${line1:2}"
echo "${line2:2}"
echo "${line3:2}"
echo "${line4:2}"
echo "${line5:2}"
echo "${line6:2}"
echo "${line7:2}"
echo "${line8:2}"
